// 1: ERROR: errors
// 2: WARN: errors and warnings
// 3: INFO: errors, warnings and informational (default)
// 4: DEBUG: errors, warnings, informational and debug
#define _WEBSOCKETS_LOGLEVEL_     3

#endif      //defines_h